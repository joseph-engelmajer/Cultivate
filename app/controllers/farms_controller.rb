class FarmsController < ApplicationController
		
protect_from_forgery with: :null_session, only: [:get_farms]

#===============================================================================
#===============================================================================

	def show
		@farm = Farm.find_by(id: params[:id])
	end

#===============================================================================
#===============================================================================

	def get_farms
		farms = Farm.all

		render json: farms
	end

#===============================================================================
#===============================================================================

end
